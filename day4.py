num1 = 15
num2 = 4

# basic arithmetic operators #
print ("\n--Arithmetic Operators--")
print ("The sum of 15 and 4 = ", num1 + num2)
print ("The difference of 15 and 4 = ", num1 - num2)
print ("The product of 15 and 4 = ", num1 * num2)
print ("15 divided by 4 = ", num1 / num2)
print ("Modulus (15 % 4) = ", num1 % num2)
print ("Exponentiation (15 ** 4) = ", num1 ** num2)

# relational operators #
print ("\n--Relational Operators--")
print ("15 is equal to 4? ", num1 == num2)
print ("15 is not equal to 4: ", num1 != num2)
print ("15 is greater than 4: ", num1 > num2)
print ("15 is less than 4: ", num1 < num2)
print ("4 is greater than or equal to 15: ", num2 >= num1)

